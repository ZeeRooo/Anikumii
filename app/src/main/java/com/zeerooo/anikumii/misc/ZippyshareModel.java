package com.zeerooo.anikumii.misc;

public class ZippyshareModel {

    private String var, value;

    public ZippyshareModel(String var, String value) {
        this.var = var;
        this.value = value;
    }

    public String getVar() {
        return var;
    }

    public String getValue() {
        return value;
    }
}
